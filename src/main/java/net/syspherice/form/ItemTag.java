package net.syspherice.form;

public class ItemTag {
	private String itemTagID;
	private String itemID;
	private String tagID;
	private String accountID;
	private String collectionName;
	
	public String getItemTagID() {
		return itemTagID;
	}

	public void setItemTagID(String itemTagID) {
		this.itemTagID = itemTagID;
	}

	public String getItemID() {
		return itemID;
	}

	public void setItemID(String itemID) {
		this.itemID = itemID;
	}

	public String getTagID() {
		return tagID;
	}

	public void setTagID(String tagID) {
		this.tagID = tagID;
	}

	public String getAccountID() {
		return accountID;
	}

	public void setAccountID(String accountID) {
		this.accountID = accountID;
	}

	public String getCollectionName() {
		return collectionName;
	}

	public void setCollectionName(String collectioName) {
		this.collectionName = collectioName;
	}

}
